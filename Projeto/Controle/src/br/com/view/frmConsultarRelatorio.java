public void calcular(){
        String mes_producao = ftfMesProducao.getText();
        String mes_enrolacao = ftfMesEnrolacao.getText();
        String quinzena_pro = tfQuinzenaProducao.getText();
        String quinzena_enro = tfQuinzenaEnrolacao.getText();        
        
        if (mes_producao.equals(mes_enrolacao) && quinzena_pro.equals(quinzena_enro)){
            ftfMesCalculo.setText(mes_producao);
            tfQuinzenaCalculo.setText(tfQuinzenaProducao.getText());
            
            //produção
            double quantidade_grande_pro = Double.parseDouble(tfQuantidadeProducaoGrande.getText());
            double quantidade_pequena_pro = Double.parseDouble(tfQuantidadeProducaoPequena.getText());
            double quantidade_paradinha_pro = Double.parseDouble(tfQuantidadeProducaoParadinha.getText());
        
            double resultado_producao = quantidade_grande_pro + quantidade_pequena_pro + quantidade_paradinha_pro;
            tfQuantidadeProducaoCalculo.setText(String.valueOf(resultado_producao));
            
            //enrolação
            double quantidade_grande_enro = Double.parseDouble(tfQuantidadeEnrolacaoGrande.getText());
            double quantidade_pequena_enro = Double.parseDouble(tfQuantidadeEnrolacaoPequena.getText());
            double quantidade_paradinha_enro = Double.parseDouble(tfQuantidadeEnrolacaoParadinha.getText());
            
            double resultado_enrolacao = quantidade_grande_enro + quantidade_pequena_enro + quantidade_paradinha_enro;
            tfQuantidadeEnrolacaoCalculo.setText(String.valueOf(resultado_enrolacao));
            
            //Passou
            double quantidade_enro_calc = Double.parseDouble(tfQuantidadeEnrolacaoCalculo.getText());
            double quantidade_pro_calc = Double.parseDouble(tfQuantidadeProducaoCalculo.getText());
            
            double calculo_result = quantidade_pro_calc - quantidade_enro_calc;
            
            tfCalculo.setText(String.valueOf(calculo_result));
        } else {
            JOptionPane.showMessageDialog(this, "Os MESES ou as QUINZENAS não conrespondem", "Validação", JOptionPane.ERROR_MESSAGE);

        } 
}


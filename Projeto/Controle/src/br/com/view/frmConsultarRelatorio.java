    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
       String mes_calculo = ftfMesCalculo.getText();

        if(mes_calculo.equals("  /    ")){
            JOptionPane.showMessageDialog(this, "O campo MÊS não está formatado", "Validação", JOptionPane.ERROR_MESSAGE );
        }

        if(tfQuinzenaCalculo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "O campo QUINZENA não está formatado", "Validação", JOptionPane.ERROR_MESSAGE);
        }

        if(tfQuantidadeProducaoCalculo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "O campo QUANTIDADE DA PRODUÇÃO não está formatado", "Validação", JOptionPane.ERROR_MESSAGE);
        }

        if(tfQuantidadeEnrolacaoCalculo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "O campo QUANTIDADE DA ENROLAÇÃO não está formatado", "Validação", JOptionPane.ERROR_MESSAGE);
        }
        
        if(tfCalculo.getText().equals("")){
            JOptionPane.showMessageDialog(this, "O campo CALCULO não está formatado", "Validação", JOptionPane.ERROR_MESSAGE);
        }
        
        if(tfPassou.getText().equals("")){
            JOptionPane.showMessageDialog(this, "O campo PASSOU EM não está formatado", "Validação", JOptionPane.ERROR_MESSAGE);
        }
        else {
            int b = JOptionPane.showConfirmDialog(null, "Deseja Cadastrar o Cálculo?" , "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (b == 0){
                try{
                    Calculo calculo = new Calculo();
                    calculo.setMes(mes_calculo);
                    calculo.setQuinzena(tfQuinzenaCalculo.getText());
                    calculo.setQuantidade_producao(tfQuantidadeProducaoCalculo.getText());
                    calculo.setQuantidade_enrolacao(tfQuantidadeEnrolacaoCalculo.getText());
                    calculo.setCalculo(tfCalculo.getText());
                    calculo.setPassou(tfPassou.getText());
                    dao3.addCalculo(calculo);
                    JOptionPane.showMessageDialog(this, "Cálculo Cadastrada com SUCESSO!" , "SUCESSO" , JOptionPane.INFORMATION_MESSAGE);
                    setCamposSalvar();
                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Erro ao Cadastrar o Cálculo", "ERRO" + ex, JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btSalvarActionPerformed


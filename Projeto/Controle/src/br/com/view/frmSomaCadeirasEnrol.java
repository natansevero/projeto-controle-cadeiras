public void somar(){
        String mes_enrolacao = ftfMesEnrolacao.getText();        
        
        tfIdEnroladorSoma.setText(tfIdEnrolador.getText());
        ftfMesEnrolacaoSoma.setText(mes_enrolacao);
        tfQuinzenaSoma.setText(tfQuinzena.getText());
        
        double quantidade_grande = Double.parseDouble(tfQuantidadeGrande.getText());
        double quantidade_pequena = Double.parseDouble(tfQuantidadePequena.getText());
        double quantidade_paradinha = Double.parseDouble(tfQuantidadeParadinha.getText());
        
        double soma = quantidade_grande + quantidade_pequena + quantidade_paradinha;
        tfSoma.setText(String.valueOf(soma));
                       
}


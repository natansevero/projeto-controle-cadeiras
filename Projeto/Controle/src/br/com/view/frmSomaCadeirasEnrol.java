public void Pesquisa_loadInitialData() {
        try {
            String pesquisa_mes_enro = ftfPesquisaMesEnrolacao.getText();
            Soma som = new Soma();
            som.setId_enrolador(tfIdEnroladorPesquisa.getText());
            som.setMes_enrolacao(pesquisa_mes_enro);
            som.setQuinzena(tfQuinzenaPesquisa.getText());
            soma = dao.searchSoma(som.getId_enrolador(), som.getMes_enrolacao(), som.getQuinzena());
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }        
        jTable1.setModel(new MyTableModel(Soma.class, soma, jTable1));
        
    }


public void loadInitialData() {
        try {
            vendas = dao.getallVendas();
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }        
        jTable1.setModel(new MyTableModel(Vendas.class, vendas, jTable1));
        
}

public void Pesquisa_loadInitialData() {
        try {
            String pesquisa_mes_venda = ftfPesquisaMesVenda.getText();
            Vendas ven = new Vendas();
            ven.setId_cliente(tfIdClientePesquisa.getText());
            ven.setMes_venda(pesquisa_mes_venda);
            ven.setQuinzena(tfQuinzenaPesquisa.getText());
            vendas = dao.searchVendas(ven.getId_cliente(), ven.getMes_venda(), ven.getQuinzena());
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }        
        jTable1.setModel(new MyTableModel(Vendas.class, vendas, jTable1));
        
    }


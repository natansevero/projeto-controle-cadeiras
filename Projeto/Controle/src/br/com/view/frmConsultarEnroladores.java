    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        if(tfIdEnrolador.getText().equals("")){
            JOptionPane.showMessageDialog(this, "O campo ID ENROLADOR não está formatado", "Validação", JOptionPane.ERROR_MESSAGE );
        }

        if(tfNomeEnrolador.getText().equals("")){
            JOptionPane.showMessageDialog(this, "O campo NOME DO ENROLADOR não está formatado", "Validação", JOptionPane.ERROR_MESSAGE);
        }
        else {
            int b = JOptionPane.showConfirmDialog(null, "Deseja EDITAR o Enrolador?" , "Confirmação", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (b == 0){
                try{
                    Enroladores enroladores = new Enroladores();
                    enroladores.setId_enrolador(tfIdEnrolador.getText());
                    enroladores.setNome_enrolador(tfNomeEnrolador.getText());                    
                    daoEnroladores.editEnroladores(enroladores);
                    JOptionPane.showMessageDialog(this, "Enrolador Editado com SUCESSO!" , "SUCESSO" , JOptionPane.INFORMATION_MESSAGE);
                    setCampos();
                    loadInitialData();
                } catch (Exception ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Erro ao Editar o Enrolador", "ERRO" + ex, JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btEditarActionPerformed

